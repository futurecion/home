function refreshLanguage(lang) {
    let data;
    if (lang === 'en') {
        $('#lang-selected img').attr('src', 'images/lang-en.jpg').attr('alt', 'English');
        $('#lang-selected b').empty().append('English');
        data = language.en;
    } else if (lang === 'jp') {
        $('#lang-selected img').attr('src', 'images/lang-jp.png').attr('alt', 'Japanese');
        $('#lang-selected b').empty().append('日本語');
        data = language.jp;
    } else if (lang === 'de') {
        $('#lang-selected img').attr('src', 'images/lang-de.png').attr('alt', 'Deutsch');
        $('#lang-selected b').empty().append('Deutsch');
        data = language.de;
    } else if (lang === 'fr') {
        $('#lang-selected img').attr('src', 'images/lang-fr.png').attr('alt', 'French');
        $('#lang-selected b').empty().append('Français');
        data = language.fr;
    } else if (lang === 'cn') {
        $('#lang-selected img').attr('src', 'images/lang-cn.png').attr('alt', 'Chinese');
        $('#lang-selected b').empty().append('中文');
        data = language.cn;
    } else {
        return;
    }
    $('[i18n]').each(function (idx, item){
        let name = $(item).attr('i18n');
        let value = data[name];
        $(item).empty().append(value);
    });
}